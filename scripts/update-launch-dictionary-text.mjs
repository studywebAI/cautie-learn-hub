const files = await fs.readdir(dir);
for (const name of files) {
  if (!name.endsWith('.json')) continue;
  const locale = name.replace('.json', '');
  const map = updates[locale];
  if (!map) continue;
  const full = path.join(dir, name);
  const raw = await fs.readFile(full, 'utf8');
  const json = JSON.parse(raw);

  if (json.settings?.developer) {
    json.settings.developer.description = map.devDescription;
    json.settings.developer.roleSwitch = map.roleSwitch;
    json.settings.developer.roleSwitchDescription = map.roleSwitchDescription;
  }

  if (json.submissions) {
    json.submissions.fileUploadComing = map.fileUpload;
  }

  await fs.writeFile(full, `${JSON.stringify(json, null, 2)}\n`, 'utf8');
}

